    public function paginate(int $page = 1, int $limit = 10): Paginator
    {
        $query = $this
            ->createQueryBuilder('b')
            ->setFirstResult(($page - 1) * $limit)
            ->setMaxResults($limit)
            ->getQuery();

        return new Paginator($query);
    }

    public function delete(Book $book): void
    {
        $em = $this->getEntityManager();
        $em->remove($book);
        $em->flush();
    }
